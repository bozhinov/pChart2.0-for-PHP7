namespace pChart\Barcodes\Linear;

class Code39 {

	public function encode($data, $opts)
	{
		if(strtoupper($opts['mode']) ==  "ASCII"){
			return $this->code_39_ascii_encode($data);
		} else {
			return $this->code_39_encode($data);
		}
	}

	private function code_39_encode($data)
	{
		$data = strtoupper(preg_replace('/[^0-9A-Za-z%$\/+ .-]/', '', $data));
		$data = str_split($data);
		$blocks = [];
		/* Start */
		$blocks[] = [
			'm' => [
				[1, 1, 1], [0, 1, 2], [1, 1, 1],
				[0, 1, 1], [1, 1, 2], [0, 1, 1],
				[1, 1, 2], [0, 1, 1], [1, 1, 1],
			],
			'l' => ['*']
		];
		/* Data */
		foreach($data as $char){
			$blocks[] = [
				'm' => [[0, 1, 3]]
			];
			$block = $this->code_39_alphabet[$char];
			$blocks[] = [
				'm' => [
					[1, 1, $block[0]],
					[0, 1, $block[1]],
					[1, 1, $block[2]],
					[0, 1, $block[3]],
					[1, 1, $block[4]],
					[0, 1, $block[5]],
					[1, 1, $block[6]],
					[0, 1, $block[7]],
					[1, 1, $block[8]],
				],
				'l' => [$char]
			];
		}
		$blocks[] = [
			'm' => [[0, 1, 3]]
		];
		/* End */
		$blocks[] = [
			'm' => [
				[1, 1, 1], [0, 1, 2], [1, 1, 1],
				[0, 1, 1], [1, 1, 2], [0, 1, 1],
				[1, 1, 2], [0, 1, 1], [1, 1, 1],
			],
			'l' => ['*']
		];
		/* Return */
		return $blocks;
	}

	private function code_39_ascii_encode($data)
	{
		$data = str_split($data);
		$modules = [
			[1, 1, 1], [0, 1, 2], [1, 1, 1],
			[0, 1, 1], [1, 1, 2], [0, 1, 1],
			[1, 1, 2], [0, 1, 1], [1, 1, 1]
		];
		/* Data */
		$label = '';
		foreach($data as $char){
			$ch = ord($char);
			if ($ch < 128) {
				$label .= ($ch < 32 || $ch >= 127) ? ' ' : $char;
				$ch = str_split($this->code_39_asciibet[$ch]);
				foreach($ch as $c){
					$b = $this->code_39_alphabet[$c];
					$modules[] = [0, 1, 3];
					$modules[] = [1, 1, $b[0]];
					$modules[] = [0, 1, $b[1]];
					$modules[] = [1, 1, $b[2]];
					$modules[] = [0, 1, $b[3]];
					$modules[] = [1, 1, $b[4]];
					$modules[] = [0, 1, $b[5]];
					$modules[] = [1, 1, $b[6]];
					$modules[] = [0, 1, $b[7]];
					$modules[] = [1, 1, $b[8]];
				}
			}
		}
		$modules[] = [0, 1, 3];
		/* End */
		$modules[] = [1, 1, 1];
		$modules[] = [0, 1, 2];
		$modules[] = [1, 1, 1];
		$modules[] = [0, 1, 1];
		$modules[] = [1, 1, 2];
		$modules[] = [0, 1, 1];
		$modules[] = [1, 1, 2];
		$modules[] = [0, 1, 1];
		$modules[] = [1, 1, 1];

		return [['m' => $modules, 'l' => [$label]]];
	}

